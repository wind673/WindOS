/* 全局变量定义 --------------------------------------------------------------*/
extern Type_Addr AppHead;
Type_Addr *pAppRun = &AppHead;

/* 全局函数编写 --------------------------------------------------------------*/
/**----------------------------------------------------------------------------
* @FunctionName  : OS_Run()     
* @Description   : None 
* @Data          : 2016/3/15
* @Explain       : None
------------------------------------------------------------------------------*/
void OS_Run(void)
{
	if(pAppRun ->next == NULL) pAppRun = &AppHead;
	else pAppRun = pAppRun ->next;  
	pAppRun ->AppFun(); //运行堆栈的程序 
}




