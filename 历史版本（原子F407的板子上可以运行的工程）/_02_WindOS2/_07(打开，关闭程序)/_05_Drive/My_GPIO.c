/**----------------------------------------------------------------------------
* @FunctionName  : My_GPIO_Init()     
* @Description   : None 
* @Data          : 2016/3/15
* @Explain       : None
------------------------------------------------------------------------------*/
int My_GPIO_Init(void)
{
	 GPIO_InitTypeDef GPIO_InitStructure;
	
	
	//选择初始化的引脚 //可以GPIO_Pin_All
	//=========================================================================
	GPIO_InitStructure.GPIO_Pin=GPIO_Pin_9|GPIO_Pin_10;										//@1
	//=========================================================================
	
	
	//最高输出频率
	//=========================================================================
	GPIO_InitStructure.GPIO_Speed=GPIO_Speed_100MHz; 
	//=========================================================================
	
	
	//引脚模式
	//=========================================================================
	 GPIO_InitStructure.GPIO_Mode=GPIO_Mode_OUT; 
	//=========================================================================


	//电流风格
	//=========================================================================
	GPIO_InitStructure.GPIO_OType=GPIO_OType_PP; 
	//=========================================================================
		
		
	//引脚上位初始状态
	//========================================================================= 
	 GPIO_InitStructure.GPIO_PuPd=GPIO_PuPd_UP; 
	//=========================================================================
	
	
	//选择开启时钟
	//=========================================================================
	RCC_AHB1PeriphClockCmd(RCC_AHB1Periph_GPIOF, ENABLE);							//@2
	//=========================================================================
		
	
	//执行初始化
	//=========================================================================
	GPIO_Init(GPIOF,&GPIO_InitStructure);											//@3
	//=========================================================================
	
//	GPIO_InitStructure.GPIO_Pin=GPIO_Pin_4|GPIO_Pin_5|GPIO_Pin_6|GPIO_Pin_7;
//	RCC_AHB1PeriphClockCmd(RCC_AHB1Periph_GPIOC, ENABLE);	
//	GPIO_Init(GPIOC,&GPIO_InitStructure);		
	
return 0;
}



